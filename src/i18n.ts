// Simple translator function that falls back to English string if Arabic version is not found
export function getTranslation(key: string, lang: 'en' | 'ar'): string {
  if (lang === 'en') return key;
  return arTranslations[key] || key;
}

// Translate dynamic speech strings spoken by the mascot
export function translateSpeech(text: string, lang: 'en' | 'ar'): string {
  if (lang === 'en') return text;
  
  // 1. Direct match
  if (arTranslations[text]) return arTranslations[text];

  // 2. Keep searching default speech
  if (text.includes("Keep searching, Detective!")) {
    return "استمر في البحث يا حضرة المحقق! يمكنك فعلها! 🔍🍌";
  }

  // 3. Level launch
  const levelMatch = text.match(/^Level (\d+): (.+?)! Let's find the 5 sneaky tweaks! 🚀$/);
  if (levelMatch) {
    const levelId = levelMatch[1];
    const levelTitle = levelMatch[2];
    const translatedTitle = arTranslations[levelTitle] || levelTitle;
    return `المستوى ${levelId}: ${translatedTitle}! لنكتشف الفروق الخمسة المخفية! 🚀`;
  }

  // 4. Hint trigger
  const hintMatch = text.match(/^Aha! Look over here: "(.+?)"$/);
  if (hintMatch) {
    const hintText = hintMatch[1];
    const translatedHint = arTranslations[hintText] || hintText;
    return `أها! انظر هنا: "${translatedHint}"`;
  }

  // 5. Success click
  const successMatch = text.match(/^Awesome! You found: (.+?)! Only (\d+) more left! ⭐$/);
  if (successMatch) {
    const diffName = successMatch[1];
    const remaining = successMatch[2];
    const translatedName = arTranslations[diffName] || diffName;
    return `رائع! لقد وجدت: ${translatedName}! متبقي ${remaining} فروق فقط! ⭐`;
  }

  // 6. Victory option 1
  const vic1Match = text.match(/^Incredible! You spotted all the changes in just (\d+) seconds! You are a master explorer! 🏆🍌$/);
  if (vic1Match) {
    const seconds = vic1Match[1];
    return `مذهل! لقد اكتشفت جميع الفروق في ${seconds} ثانية فقط! أنت مستكشف بارع! 🏆🍌`;
  }

  // 7. Victory option 2
  const vic2Match = text.match(/^Sensational! Nano Banana is super proud of your hawk eyes! You earned (\d+) stars! ⭐⭐⭐$/);
  if (vic2Match) {
    const stars = vic2Match[1];
    return `رائع! نانو بنانا فخور جداً بعينيك الصقريتين! لقد كسبت ${stars} نجوم! ⭐⭐⭐`;
  }

  // 8. Welcome speech
  if (text.includes("Welcome back to the Playroom!")) {
    return "مرحباً بك مجدداً في غرفة الألعاب! اختر مستوى ودعنا نبدأ البحث! 🔍🍌";
  }

  return text;
}
